package com.csstest.teamb.VO;

public class lodgingVO {
	
	int lodgingNo;
	String title;
	String latitude;
	String hardness;
	String lodging_img;
	String addr;
	int zip_code;
	String street_number;
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getHardness() {
		return hardness;
	}
	public void setHardness(String hardness) {
		this.hardness = hardness;
	}
	public String getLodging_img() {
		return lodging_img;
	}
	public void setLodging_img(String lodging_img) {
		this.lodging_img = lodging_img;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public int getZip_code() {
		return zip_code;
	}
	public void setZip_code(int zip_code) {
		this.zip_code = zip_code;
	}
	public String getStreet_number() {
		return street_number;
	}
	public void setStreet_number(String street_number) {
		this.street_number = street_number;
	}
	public int getLodgingNo() {
		return lodgingNo;
	}
	public void setLodgingNo(int lodgingNo) {
		this.lodgingNo = lodgingNo;
	}
	
	
}
